package com.basefrm.excel.config;

public class KeyUpValue {
    private String begin;
    private String end;

    public KeyUpValue() {
    }

    public String getBegin() {
        return begin;
    }

    public void setBegin(String begin) {
        this.begin = begin;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
}
